# Give three more examples of Mathematical function and how they are implemented in Python?
from math import exp
x = 8
exponential_result = exp(x)  
print(exponential_result)  

from math import pow
base = 3
exponent = 2  
power_result = pow(base, exponent)  
print(power_result)

from math import atan
x = 1
atan_result = atan(x)
print(atan_result)

